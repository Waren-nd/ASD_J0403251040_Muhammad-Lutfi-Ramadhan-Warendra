def pangkat(a, n):
    # Base case:
    # Jika pangkat 0, hasilnya selalu 1
    if n == 0:
        return 1

    # Recursive case:
    # a^n = a Ã— a^(n-1)
    return a * pangkat(a, n - 1)


print(pangkat(2, 4))  # Output: 16